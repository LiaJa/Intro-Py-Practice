mood = "happy"
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Hope you feel better soon!")
elif mood == "excited":
    print("Keep it up and make it work!")
elif mood == "relaxed":
    print("Float in your inner peace :)")
else:
    print("I don't recognize this mood.")